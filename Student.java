public class Student {
    String name;
    int age;
    Student(String name,int age)
    {
        this.name=name;
        this.age=age;
    }
    public void setName(String name,int age)
    {
        this.name=name;
        this.age=age;
    }
    public String getName()
    {
        return name;
    }
     public void setAge(int age)
    {
        this.age=age;
    }
    public int getAge()
    {
        return age;
    }
    
}
